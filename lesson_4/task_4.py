
while True:
    try:
        A = int(input("Ведіть рік для перевірки (від 1900 до 1000000 року):"))
        break
    except:
        print("Введіть ЧИСЛОВЕ значення року!")

if A < 1900 or A > 1_000_000:
    print("Рік не відповідає умовам!")
elif A % 4 == 0 and A % 100 != 0 or A % 400 == 0:
    print(A, "високосний рік")
else:
    print(A, "не високосний рік")
